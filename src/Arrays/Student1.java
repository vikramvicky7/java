package Arrays;

public class Student1 {
	int id;
	String name;
	float percentage;

	public Student1(int id, String name, float percentage) {
		super();
		this.id = id;
		this.name = name;
		this.percentage = percentage;
	}

	public void printd() {
		System.out.println("id=" + id);
		System.out.println("name =" + name);
		System.out.println("percentage =" + percentage);
	}

}
